const catalogs: Record<Locale, Record<string, string>> = { fr, en, ar, pt, sw };

function interpolate(text: string, params?: MessageParams): string {
  if (!params) return text;
  return text.replace(/\{(\w+)\}/g, (_, key: string) => {
    const val = params[key];
    return val === undefined || val === null ? `{${key}}` : String(val);
  });
}

export function translate(locale: Locale, key: string, params?: MessageParams): string {
  const raw =
    catalogs[locale]?.[key] ??
    catalogs.en[key] ??
    catalogs.fr[key] ??
    key;
  return interpolate(raw, params);
}

/** @deprecated Préférer useTranslation() ou translate() */
export function t(locale: Locale, key: string, params?: MessageParams): string {
  return translate(locale, key, params);
}

